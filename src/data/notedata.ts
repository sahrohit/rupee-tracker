export const YEARS = [
	...new Set(NOTE_DATA.map((note) => note.year).sort((a, b) => a - b)),
];

export const noteFromDenomination = (denomination: number, year?: number) => {
	if (year && year in YEARS) {
		return NOTE_DATA.find(
			(note) => note.denomination_value === denomination && note.year === 2019
		);
	} else {
		return NOTE_DATA.sort((a, b) => b.year - a.year).find(
			(note) => note.denomination_value === denomination
		);
	}
};
